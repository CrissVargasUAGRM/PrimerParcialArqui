    private void listar(){
        tablaPacientes.setModel(PACIENTE.listar());
    }
    
    private void insertar(){
        String resp;
        DCliente cliente = (DCliente)cboCliente.getSelectedItem();
        DDoctor doctor = (DDoctor)cboDoctor.getSelectedItem();
        resp = this.PACIENTE.insertar(txtNombre.getText(), txtEdad.getText(), txtEspecie.getText(), txtRaza.getText(), doctor.getId(), cliente.getId());
        if(resp.equals("OK")){
            this.mensajeOk("Insertado el paciente");
            this.limpiar();
            this.listar();
        }else{
            this.mensajeError(resp);
        }
    }
    
    private void editar(){
        String resp;
        DCliente cliente = (DCliente)cboCliente.getSelectedItem();
        DDoctor doctor = (DDoctor)cboDoctor.getSelectedItem();
        resp = this.PACIENTE.editar(txtNombre.getText(), txtEdad.getText(), txtEspecie.getText(), txtRaza.getText(), doctor.getId(), cliente.getId(), Integer.parseInt(txtid.getText()));
        if(resp.equals("OK")){
            this.mensajeOk("Editado");
            this.limpiar();
            this.listar();
        }else{
            this.mensajeError(resp);
        }
    }
    
    private void eliminar(int id){
        String resp;
        resp = PACIENTE.eliminar(id);
        if(resp.equals("OK")){
            this.mensajeOk("Eliminado");
            this.listar();
        }else{
            this.mensajeError(resp);
        }
    }
    
    private void limpiar(){
        txtNombre.setText("");
        txtEdad.setText("");
        txtEspecie.setText("");
        txtRaza.setText("");
        this.accion = "guardar";
    }
    
    private void cargarDoctores(){
        DefaultComboBoxModel items = this.DOCTOR.doctores();
        cboDoctor.setModel(items);
    }
    
    private void cargarClientes(){
        DefaultComboBoxModel items = this.CLIENTE.clientes();
        cboCliente.setModel(items);
    }
    
    private void mensajeError(String mansaje){
        JOptionPane.showMessageDialog(this, mansaje, "Sistema", JOptionPane.ERROR_MESSAGE);
    }
    
    private void mensajeOk(String mansaje){
        JOptionPane.showMessageDialog(this, mansaje, "Sistema", JOptionPane.INFORMATION_MESSAGE);
    }

