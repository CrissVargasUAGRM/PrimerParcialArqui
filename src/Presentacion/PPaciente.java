    private void btnCargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarActionPerformed
        // TODO add your handling code here:
        if(tablaPacientes.getSelectedRowCount() == 1){
            String id = String.valueOf(tablaPacientes.getValueAt(tablaPacientes.getSelectedRow(), 0));
            String nombre = String.valueOf(tablaPacientes.getValueAt(tablaPacientes.getSelectedRow(), 1));
            String edad = String.valueOf(tablaPacientes.getValueAt(tablaPacientes.getSelectedRow(), 2));
            String especie = String.valueOf(tablaPacientes.getValueAt(tablaPacientes.getSelectedRow(), 3));
            String raza = String.valueOf(tablaPacientes.getValueAt(tablaPacientes.getSelectedRow(), 4));
            
            txtid.setText(id);
            txtNombre.setText(nombre);
            txtEdad.setText(edad);
            txtEspecie.setText(especie);
            txtRaza.setText(raza);
            
            this.accion = "editar";
            btnGuardar.setText("Editar");
        }else{
            this.mensajeError("Selecciona un paciente para editar");
        }
    }//GEN-LAST:event_btnCargarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        String id = String .valueOf(tablaPacientes.getValueAt(tablaPacientes.getSelectedRow(), 0));
        if(JOptionPane.showConfirmDialog(this, "Deseas eliminar al paciente", "Eliminar", JOptionPane.YES_NO_OPTION) == 0){
            this.eliminar(Integer.parseInt(id));
        }else{
            this.mensajeError("Seleccion un registro a eliminar");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed


